function getRandomItem(array) {
  return array[Math.floor(Math.random() * array.length)];
}

app.get("/", (req, res) => {
  const singleQuotes = {};

  for (const category in quoteCategories) {
    if (quoteCategories.hasOwnProperty(category)) {
      const quotes = quoteCategories[category];
      const randomQuote = getRandomItem(quotes);
      singleQuotes[`${category}_quote`] = randomQuote;
    }
  }
  const jsonString = JSON.stringify(singleQuotes, null, 2);
  res.set("Content-Type", "application/json");
  res.send(jsonString);
});

